/**
 * Returns the appropriate crypto icon URL.
 * - Uses `cryptologos.cc` for special cases (logos with hyphens).
 * - Defaults to `jsdelivr` for standard cryptocurrencies.
 */
function getCryptoIcon(name: string): string {
  return name.includes("-") // More reliable way to detect special cases
    ? `https://cryptologos.cc/logos/${name}.svg?v=040`
    : `https://cdn.jsdelivr.net/gh/atomiclabs/cryptocurrency-icons/svg/color/${name.toLowerCase()}.svg`;
}
